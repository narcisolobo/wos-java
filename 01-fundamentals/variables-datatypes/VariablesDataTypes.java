public class VariablesDataTypes {
    public static void main(String[] args) {
        int age = 23;
        System.out.println(age);

        double pi = 3.14;
        System.out.println(pi);

        String myName = "Narciso";
        System.out.println(myName);
        System.out.println(myName.getClass().getName());
    }
}
